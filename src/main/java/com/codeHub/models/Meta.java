package com.codeHub.models;

public class Meta {
    private String city;
    private String last_name;
    private String first_name;
    private String age;

    public Meta(String city, String last_name, String first_name, String age) {
        this.city = city;
        this.last_name = last_name;
        this.first_name = first_name;
        this.age = age;
    }
}
